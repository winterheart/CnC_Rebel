#include <cstring>
#include "nstrdup.h"

/**************************************************************************
 * nstrdup -- duplicates a string using new[]                             *
 *                                                                        *
 * INPUT:                                                                 *
 *                                                                        *
 * OUTPUT:                                                                *
 *                                                                        *
 * WARNINGS:                                                              *
 *                                                                        *
 * HISTORY:                                                               *
 *   02/03/1998 PWG : Created.                                            *
 *========================================================================*/
char *nstrdup(const char *str) {
  if (str == nullptr)
    return nullptr;

  // eventually should be replaced with NEW when we go to the wwnew stuff.
  char *retval = new char[strlen(str) + 1];
  strcpy(retval, str);
  return retval;
}
